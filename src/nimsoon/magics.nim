const
  RookMaxBits   = 12   # 2^12 = 4096
  BishopMaxBits = 9    # 2^9  = 512
type
  RookTable*   = array[64, array[1 shl RookMaxBits,   Bitboard]]
  BishopTable* = array[64, array[1 shl BishopMaxBits, Bitboard]] 
  MagicParams* = object
    mask* : Bitboard
    magic*: uint64
    shift*: int 

## Getting blockers
type Direction* = enum
  dirN, dirNE, dirE, dirSE, dirS, dirSW, dirW, dirNW

const
  DirOffsets: array[Direction, int] = [8, 9, 1, -7, -8, -9, -1, 7]
  IsPositive: array[Direction, bool] = [true, true, true, false, false, false, false, true]

func computeRays(): array[Direction, array[64, Bitboard]] {.compileTime.} =
  for dir in Direction:
    let off = DirOffsets[dir]
    for sq in 0..63:
      var cur = sq
      while true:
        let nxt = cur + off
        if nxt notin 0..63: break
        if abs((nxt and 7) - (cur and 7)) > 1: break
        result[dir][sq].setBit(nxt)
        cur = nxt

const Rays*: array[Direction, array[64, Bitboard]] = computeRays()

func rayAttack(sq: Square, dir: Direction, occupied: Bitboard): Bitboard {.inline.} =
  let ray      = Rays[dir][sq]
  let blockers = ray and occupied
  if blockers == 0: return ray
  let blockSq = if IsPositive[dir]: lsb(blockers) else: msb(blockers)
  ray xor Rays[dir][blockSq]

func rookSlowAttacks(sq: Square, occupied: Bitboard): Bitboard {.inline.} =
  rayAttack(sq, dirN, occupied) or rayAttack(sq, dirE, occupied) or
  rayAttack(sq, dirS, occupied) or rayAttack(sq, dirW, occupied)

func bishopSlowAttacks(sq: Square, occupied: Bitboard): Bitboard {.inline.} =
  rayAttack(sq, dirNE, occupied) or rayAttack(sq, dirNW, occupied) or
  rayAttack(sq, dirSE, occupied) or rayAttack(sq, dirSW, occupied)
    
iterator subsets(mask: Bitboard): Bitboard {.inline.} =
  ## Yields all 2^popcount(mask) subsets of mask, starting with 0.
  ## See: https://www.chessprogramming.org/Traversing_Subsets_of_a_Set
  var subset = Bitboard(0)
  yield subset                           # the empty set (no blockers)
  while true:
    subset = (subset - mask) and mask
    if subset == 0: break
    yield subset 
    
func buildRookMagics(): (array[64, MagicParams], RookTable) {.compileTime.} =
  for sq in 0..63:
    let mask  = KannanRookMasks[sq]
    let magic = KannanRookMagics[sq]
    let shift = KannanRookShifts[sq]
    
    # Store the parameters for runtime retrieval
    result[0][sq] = MagicParams(mask: mask, magic: magic, shift: shift)

    # Populate the lookup table using the guaranteed magic
    for subset in mask.subsets:
      let atk = rookSlowAttacks(sq, subset)
      let idx = int((subset * magic) shr shift)
      result[1][sq][idx] = atk

func buildBishopMagics(): (array[64, MagicParams], BishopTable) {.compileTime.} =
  for sq in 0..63:
    let mask  = KannanBishopMasks[sq]
    let magic = KannanBishopMagics[sq]
    let shift = KannanBishopShifts[sq]
    
    # Store the parameters for runtime retrieval
    result[0][sq] = MagicParams(mask: mask, magic: magic, shift: shift)

    # Populate the lookup table using the guaranteed magic
    for subset in mask.subsets:
      let atk = bishopSlowAttacks(sq, subset)
      let idx = int((subset * magic) shr shift)
      result[1][sq][idx] = atk

const
  (RookMagics, RookLookup) = buildRookMagics()
  (BishopMagics, BishopLookup) = buildBishopMagics()
      
func rookAttacks*(sq: Square, occupied: Bitboard): Bitboard {.inline.} =
  let m   = RookMagics[sq]
  let idx = int((occupied and m.mask) * m.magic shr m.shift)
  RookLookup[sq][idx]
 
func bishopAttacks*(sq: Square, occupied: Bitboard): Bitboard {.inline.} =
  let m   = BishopMagics[sq]
  let idx = int((occupied and m.mask) * m.magic shr m.shift)
  BishopLookup[sq][idx]
 
func queenAttacks*(sq: Square, occupied: Bitboard): Bitboard {.inline.} =
  rookAttacks(sq, occupied) or bishopAttacks(sq, occupied)
 
